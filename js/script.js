let currentLang = localStorage.getItem('lang') || 'zh';

document.addEventListener('DOMContentLoaded', () => {
  renderLanguage();
  bindLangButtons();
  navShrink();
  backTop();
  initSlider();
  initTab();
  initAccordion();
  formCheck();
  followMouse();
  initMobileMenu();
});

function renderLanguage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    try {
      let keys = el.getAttribute('data-i18n').split('.');
      let res = i18n[currentLang];
      keys.forEach(k => res = res[k]);
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = res;
      } else {
        el.innerText = res;
      }
    } catch (e) {}
  });
}

function bindLangButtons() {
  const langs = [
    { id: 'btn-zh', lang: 'zh' },
    { id: 'btn-zh-tw', lang: 'zh_tw' },
    { id: 'mobile-zh', lang: 'zh' },
    { id: 'mobile-zh-tw', lang: 'zh_tw' }
  ];
  langs.forEach(({ id, lang }) => {
    const btn = document.getElementById(id);
    if (btn) btn.onclick = () => {
      currentLang = lang;
      localStorage.setItem('lang', lang);
      renderLanguage();
    };
  });
}

function navShrink() {
  window.addEventListener('scroll', () => {
    document.querySelector('.navbar').classList.toggle('shrink', window.scrollY > 50);
  });
}

function backTop() {
  const btn = document.querySelector('.back-top');
  window.addEventListener('scroll', () => {
    btn.classList.toggle('show', window.scrollY > 300);
  });
  btn.onclick = () => window.scrollTo({ top: 0, behavior: 'smooth' });
}

function initSlider() {
  const slides = document.querySelectorAll('.slide-item');
  if (!slides.length) return;
  let i = 0;
  setInterval(() => {
    slides.forEach(s => s.classList.remove('active'));
    i = (i + 1) % slides.length;
    slides[i].classList.add('active');
  }, 3000);
}

function initTab() {
  const btns = document.querySelectorAll('.tab-btn');
  const panels = document.querySelectorAll('.tab-panel');
  btns.forEach((btn, idx) => {
    btn.onclick = () => {
      btns.forEach(b => b.classList.remove('active'));
      panels.forEach(p => p.classList.remove('active'));
      btn.classList.add('active');
      panels[idx].classList.add('active');
    };
  });
}

function initAccordion() {
  document.querySelectorAll('.accordion-header').forEach(h => {
    h.onclick = () => h.nextElementSibling.classList.toggle('show');
  });
}

function formCheck() {
  const form = document.querySelector('#msgForm');
  if (!form) return;
  form.onsubmit = (e) => {
    e.preventDefault();
    const name = form.name.value.trim();
    const email = form.email.value.trim();
    if (!name || !email) {
      alert(i18n[currentLang].fillInfo);
      return;
    }
    alert(i18n[currentLang].submitSuccess);
    form.reset();
  };
}

function followMouse() {
  document.addEventListener('mousemove', (e) => {
    document.querySelectorAll('.card').forEach(c => {
      const r = c.getBoundingClientRect();
      const x = e.clientX - r.left - r.width / 2;
      const y = e.clientY - r.top - r.height / 2;
      c.style.transform = `perspective(1000px) rotateY(${x/20}deg) rotateX(${-y/20}deg)`;
    });
  });
}

function initMobileMenu() {
  const toggle = document.getElementById('menuToggle');
  const menu = document.getElementById('mobileMenu');
  if (!toggle || !menu) return;
  toggle.onclick = () => menu.classList.toggle('show');
  menu.querySelectorAll('li').forEach(item => {
    item.onclick = () => menu.classList.remove('show');
  });
}
